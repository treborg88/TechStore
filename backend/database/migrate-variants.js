/**
 * migrate-variants.js
 *
 * One-time migration: adds `description` column to product_variants
 * and creates the `variant_images` table in EVERY tenant schema.
 *
 * Run:  node database/migrate-variants.js
 */
require('dotenv').config();
require('dotenv').config({ path: require('path').join(__dirname, '..', '.env.local'), override: true });

const { Pool } = require('pg');

// Minimal pool — no tenant-context wrapping needed for migrations
const pool = new Pool({
  connectionString: process.env.DATABASE_URL,
});

const SCHEMA_SQL = `
  ALTER TABLE product_variants ADD COLUMN IF NOT EXISTS description TEXT;

  CREATE TABLE IF NOT EXISTS variant_images (
      id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      variant_id  BIGINT NOT NULL REFERENCES product_variants (id) ON DELETE CASCADE,
      image_path  TEXT NOT NULL,
      sort_order  INTEGER NOT NULL DEFAULT 0,
      created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW()
  );

  CREATE INDEX IF NOT EXISTS idx_variant_images_variant ON variant_images (variant_id);
`;

async function runMigration() {
  const client = await pool.connect();
  try {
    // 1. Run in public schema
    console.log('→ Running migration in public schema...');
    await client.query(`SET search_path TO public`);
    await client.query(SCHEMA_SQL);
    console.log('  ✅ public schema done');

    // 2. List all tenant schemas
    const { rows: schemas } = await client.query(
      `SELECT schema_name FROM information_schema.schemata
       WHERE schema_name LIKE 'tenant_%'`
    );

    if (schemas.length === 0) {
      console.log('  ℹ️  No tenant schemas found. Skipping.');
    } else {
      for (const { schema_name } of schemas) {
        console.log(`→ Running migration in ${schema_name}...`);
        await client.query(`SET search_path TO "${schema_name}", public`);
        await client.query(SCHEMA_SQL);
        console.log(`  ✅ ${schema_name} done`);
      }
    }

    console.log('\n🎉 Migration complete!');
  } catch (err) {
    console.error('\n❌ Migration failed:', err.message);
    process.exit(1);
  } finally {
    client.release();
    await pool.end();
  }
}

runMigration();
